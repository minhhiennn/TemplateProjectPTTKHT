package beans;

public class Faculty {
	String idF,name;

	public Faculty(String idF, String name) {
		super();
		this.idF = idF;
		this.name = name;
	}

	public String getIdF() {
		return idF;
	}

	public void setIdF(String idF) {
		this.idF = idF;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
